using System;

    class DefineVariables
    {
        static void Main()
        {
            ushort a = 52130;
            sbyte b = -115;
            int c = 4825932;
            byte d = 97;
            short e = -10000;
            Console.WriteLine("The appropriate type for 52130 is ushort : {0}", a);
            Console.WriteLine("The appropriate type for -115 is sbyte : {0}", b);
            Console.WriteLine("The appropriate type for 4825932 is int : {0}", c);
            Console.WriteLine("The appropriate type for 97 is byte : {0}", d);
            Console.WriteLine("The appropriate type for -10000 is short : {0}", e);
        }
    }

